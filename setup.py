from setuptools import setup, find_packages
from pathlib import Path
import os

if __name__ == "__main__":
    with Path(Path(__file__).parent, "README.md").open(encoding="utf-8") as file:
        long_description = file.read()

    def _read_reqs(relpath):
        fullpath = os.path.join(os.path.dirname(__file__), relpath)
        with open(fullpath) as f:
            return [s.strip() for s in f.readlines() if (s.strip() and not s.startswith("#"))]

    REQUIREMENTS = _read_reqs("requirements.txt")

    setup(
        name="embedding_dataset_reordering",
        packages=find_packages(),
        include_package_data=True,
        version="1.0.0",
        license="MIT",
        description="Reorders the embeddings generated by CLIP to be in line with the webdatasets generated by img2dataset.",
        long_description=long_description,
        long_description_content_type="text/markdown",
        entry_points={"console_scripts": ["reorder-embeddings = embedding_dataset_reordering.cli:cli"]},
        author="Aidan Dempster",
        author_email="aidan.dempster@gmail.com",
        url="https://github.com/Veldrovive/embedding-dataset-reordering",
        data_files=[(".", ["README.md"])],
        keywords=["machine learning"],
        install_requires=REQUIREMENTS,
        classifiers=[],
    )
